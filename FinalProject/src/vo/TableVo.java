package vo;

public class TableVo {
	
	private int tablenumber;
	private int Possiblepeople;
	private int enterpeople;
	private String userId;
	
	
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public int getEnterpeople() {
		return enterpeople;
	}
	public void setEnterpeople(int enterpeople) {
		this.enterpeople = enterpeople;
	}
	public int getTablenumber() {
		return tablenumber;
	}
	public void setTablenumber(int tablenumber) {
		this.tablenumber = tablenumber;
	}
	public int getPossiblepeople() {
		return Possiblepeople;
	}
	public void setPossiblepeople(int possiblepeople) {
		Possiblepeople = possiblepeople;
	}
	

}
